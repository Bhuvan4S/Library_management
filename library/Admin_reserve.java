    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        try {

            String sql = "SELECT r.Reservation_ID, r.ISBN, b.Title, r.reserved_date, r.reserve_status " +
            "FROM Reservation r " +
            "JOIN Book_Registry b ON r.ISBN = b.ISBN " +
            "WHERE r.mem_id = ?";

            ps = con.prepareStatement(sql);
            ps.setString(1, mem_id.getText());
            rs = ps.executeQuery();

            DefaultTableModel tbmodel = (DefaultTableModel) rtable.getModel();
            tbmodel.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                String data[] = {
                    rs.getString("Reservation_ID"),
                    rs.getString("ISBN"),
                    String.valueOf(rs.getLong("ISBN")),
                    rs.getDate("reserved_date").toString(),
                    rs.getString("reserve_status")
                };
                tbmodel.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation_stats.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_searchActionPerformed

