    public Update_details(Connection con,String memId) {
        initComponents();
        this.con=con;
        this.memId=memId;
        mem_id.setText(memId);
        try {
            String sql = "SELECT * FROM members WHERE mem_id = ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, mem_id.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                name.setText(rs.getString("mem_name"));
                role.setSelectedItem(rs.getString("mem_role"));
                dept.setText(rs.getString("mem_dept"));
                email.setText(rs.getString("mem_email"));
                mobile.setText(rs.getString("mem_mobno"));

                JOptionPane.showMessageDialog(this, "Record fetched!");
            } else {
                JOptionPane.showMessageDialog(this, "Record not found!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Update_details.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
