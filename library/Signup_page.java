    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
         try {
        String memId = mem_id.getText();
        String pwd = new String(password.getText());

        // Check if mem_id exists
        String checkSql = "SELECT * FROM Mem_login WHERE mem_id = ?";
        ps = con.prepareStatement(checkSql);
        ps.setString(1, memId);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Member ID already taken.");
        } else {
            // Insert new member login
            String insertSql = "INSERT INTO Mem_login (mem_id, pwd) VALUES (?, ?)";
            ps = con.prepareStatement(insertSql);
            ps.setString(1, memId);
            ps.setString(2, pwd);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(this, "Inserted!");

            // Go to DetailsPage
            Details_page detailsPage = new Details_page(mem_id.getText());
            detailsPage.setVisible(true);
            dispose(); // Close SignUpPage
        }

        rs.close();
        ps.close();
    } catch (SQLException ex) {
        Logger.getLogger(Signup_page.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    }//GEN-LAST:event_enterActionPerformed

