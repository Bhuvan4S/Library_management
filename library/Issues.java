    private void displayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayActionPerformed
            try {

        String sql = "SELECT r.Issue_ID, r.ISBN, b.Title, r.Issue_date " +
                     "FROM Issues r " +
                     "JOIN Book_Registry b ON r.ISBN = b.ISBN " +
                     "WHERE r.mem_id = ?";
        
        ps = con.prepareStatement(sql);
        ps.setString(1, mem_id.getText());
        rs = ps.executeQuery();

        DefaultTableModel tbmodel = (DefaultTableModel) Issues_tbl.getModel();
        tbmodel.setRowCount(0); // Clear existing rows

        while (rs.next()) {
            String data[] = {
                rs.getString("Issue_ID"), 
                //rs.getString("ISBN"), 
                String.valueOf(rs.getLong("ISBN")),
                rs.getString("Title"),
                rs.getDate("Issue_date").toString(), 
            };
            tbmodel.addRow(data);
        }
    } catch (SQLException ex) {
        Logger.getLogger(Issues.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    }//GEN-LAST:event_displayActionPerformed

